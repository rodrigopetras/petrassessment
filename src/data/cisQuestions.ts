// Função para obter categorias únicas
export const getCategories = (): string[] => {
  const categories = new Set(cisQuestions.map(q => q.category));
  return Array.from(categories);
};

// Função para obter grupos de uma categoria
export const getGroupsByCategory = (category: string): string[] => {
  const groups = new Set(
    cisQuestions.filter(q => q.category === category).map(q => q.group)
  );
  return Array.from(groups);
};

// Função para filtrar perguntas por tamanho de empresa
export const filterQuestionsByCompanySize = (
  questions: Question[],
  size: 'pequena' | 'media' | 'grande'
): Question[] => {
  return questions.filter(q => {
    if (size === 'pequena') return q.pequenas;
    if (size === 'media') return q.medias;
    return q.grandes;
  });
};
